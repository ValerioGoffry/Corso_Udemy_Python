# Operatore AND = Per avere un risultato vero tutte e due le condizioni devono essere vere
# Operatore OR = Per avere un risultato vero una delle due condizioni deve essere vera
# Operatore NOT = NOT True = False | NOT False = True

# AND
print("--------------------------")
print("AND")
print(5 < 6 and 7 > 3)
print(8 == 8 and 3 == 2)
print(2 != 2 and 2 == 2)
print(2 >= 5 and 5 >= 8)
print("--------------------------")
print("OR")
# OR
print("asd" == "asd" or 2 == 3)
print("asd" == "asd" or 2 > 3)
print(2 < 1 or 5 > 4)
print(3 > 5 or 5 != 5)
print("--------------------------")
# NOT
print("NOT")
print(not"asd" == "asd")
print(not "eggs" == "bacon")
